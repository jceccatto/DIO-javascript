// tipos de funcoes

//declarativas
function funcao(){
    console.log('Tudo certo jovem!')
}
funcao();

//expressao de funcao
// com nomeacao
var funcao = function(){
    console.log('sou uma mensagem de funcao de expressao!')
}
funcao();

//arrow function
var funcao =()=>{
    console.log('sou uma arrow function!')
}
funcao();